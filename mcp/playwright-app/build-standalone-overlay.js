// Builds dist/inspector-overlay.standalone.js — the file served at
// GET /api/v1/web-wants/inspector-overlay.js for browsers that can't use the
// Chrome/CDP path (Safari, iPhone Chrome — see the desktop bookmarklet / iOS
// Shortcut loader in WebInspectorModal.tsx). Runs after `tsc` so
// dist/webInspectorOverlayCore.js already exists; pulls the real compiled
// function via require() (not text-scraping dist/*.js) so this can never
// silently drift from what the Chrome/CDP path runs.
//
// The bootstrap this wraps around the core function does what
// engine/types/agent_web_inspector.go's openInspectorTab does server-side for
// the CDP path — resolve done_webhook_url/suggest_name_url/character
// info/existing marks — by asking GET /api/v1/web-wants/active-inspection
// instead, since a static loader has no want ID to carry that per-session
// info in its own URL (see handlers_web_wants.go's activeInspection).
const fs = require('fs');
const path = require('path');
const { webInspectorOverlayCore } = require('./dist/webInspectorOverlayCore.js');

const coreSrc = webInspectorOverlayCore.toString();

const bundle = `// AUTO-GENERATED by build-standalone-overlay.js — do not edit directly.
// Source of truth: webInspectorOverlayCore.ts
(function () {
  var __webInspectorOverlayCore = ${coreSrc};

  var loaderScript = document.currentScript;
  var apiBase = loaderScript ? new URL(loaderScript.src).origin : window.location.origin;

  fetch(apiBase + '/api/v1/web-wants/active-inspection?url=' + encodeURIComponent(location.href))
    .then(function (r) {
      if (!r.ok) return r.json().then(function (d) { throw new Error(d.error || ('HTTP ' + r.status)); });
      return r.json();
    })
    .then(function (d) {
      __webInspectorOverlayCore({
        webhookUrl: d.done_webhook_url,
        suggestNameUrl: d.suggest_name_url,
        myCharacterId: d.character_id,
        myColor: d.color,
        myAvatar: d.avatar,
        existingMarks: d.existing_marks || [],
        navElements: d.nav_elements || [],
      });
    })
    .catch(function (e) {
      var banner = document.createElement('div');
      banner.style.cssText = 'position:fixed;top:12px;left:50%;transform:translateX(-50%);background:#1e293b;color:#f87171;padding:10px 16px;border-radius:8px;font:13px system-ui,sans-serif;z-index:2147483647;box-shadow:0 8px 24px rgba(0,0,0,.4);border:1px solid #475569;max-width:80vw;text-align:center';
      // active-inspection never 404s anymore (it falls back to the always-on
      // capture endpoint), so reaching here means a genuine network/server
      // problem — not a missing GUI session.
      banner.textContent = 'Web Want Inspector: ' + e.message + ' — mywantサーバーに接続できません。サーバーが起動しているか確認してください。';
      document.body.appendChild(banner);
      setTimeout(function () { banner.remove(); }, 6000);
    });
})();
`;

const outPath = path.join(__dirname, 'dist', 'inspector-overlay.standalone.js');
fs.writeFileSync(outPath, bundle);
console.log('[build-standalone-overlay] wrote', outPath, `(${bundle.length} bytes)`);
